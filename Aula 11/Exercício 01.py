#1) Escreva um programa que imprima os números de 1 a 10 usando um laço FOR. Adicione comentários ao programa 
#explicando como ele funciona. Como é seu comportamento e como você entende o funcionamento do FOR

#solução
for numero in range(1, 11): 
   print(numero)


#EXPLICAÇÃO
#O FOR (para) indica que a variável "numero" deverá ser escrita de acordo com a RANGE (faixa). 
#A RANGE define que os números devem estar contidos de 1 a 11. Devem ser repetidos 10 vezes.

