#2) Faça um programa que leia o nome do usuário e mostre esse nome 10 vezes.
#RESOLVA E EXPLIQUE

nome = str(input('Digite o nome: '))

for i in range(1, 11):
    print(nome)


#EXPLICAÇÃO
#A variável i é uma variável aleatória que representa o número de repetições que serão feitas. 
#O valor inserido pelo usuário é repetido de acordo com a variável i e a range.