#7) Leia números de 1 a 10 e p mostre em algarismo romano.

numero = int(input("Digite um número de 1 a 10: "))

if numero == 1:
    print("I")
elif numero == 2:
    print("II")
elif numero == 3:
    print("III")
elif numero == 4:
    print("IV")
elif numero == 5:
    print("V")
elif numero == 6:
    print("VI")
elif numero == 7:
    print("VII")
elif numero == 8:
    print("VIII")
elif numero == 9:
    print("IX")
elif numero == 10:
    print("X")
else:
    print('Valor inválido!')