x = 0
while x < 10:
    print(x)
    x += 1
else:
    print("fim while")

#O programa define que enquanto a variável for menor que 10, ele deve mostrar a sequência.
#Quando o while notar o contrário (falso), ou seja, x >= 10, ele deve mostrar "fim while"